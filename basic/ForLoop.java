package basic;

public class ForLoop {
    public static void main(String[] args) {
        /*
         * For loops are used when we know from where the index start, and at when we need to extend the index
         */

        // This will print table of 2
        for (int i = 1; i <= 10; i ++) {
            System.err.println("2 * " + i + " = " + (2*i));
        }
    }
}
